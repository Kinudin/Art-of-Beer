<h4>Barley</h4>
<p>
	Barley is one of the three requirements in beer for fermentation. Barley is germinated to a certain degree, at which point it is then dried. This delicate process of germination and drying develops sugars, soluble starch, and starch-to-sugar-converting enzymes.
</p>
</article>

<article>
	<h4>Water</h4>
	<p>Beer is essentially <i>90 to 95% water</i>. The quality of the water you choose is essential. Tap water is a no go, so you have the options of whether or not to go with Reverse Osmosis(RO), or buying bottled water at the grocery store. The mineral content of water is important in the brewing of beer.
	</p>
	<p>
		Tap water is out of the question when considering the fact that water and its properties interact with every process of beer making.
	</p>
</article>

<article>
<h4>Yeast</h4>
<p>
	The type of yeast that you use in brewing is as important as any other ignredient in your beer. If the wrong yeast is chosen, contaminates your beer, the result will often be strange flavors, overcarbonation, irregularly dense haze.
